// ── Scoring ──

export interface RoamResult {
  episode: RoamEpisode;
  score: number;
  isTopPick: boolean;
}

// Map each quiz region choice to the set of dataset regions it should include.
// Exported for scripts/validate-roam-data.mjs (region-honesty assertions).
export const REGION_GROUPS: Record<string, Region[]> = {
  "Americas": ["Americas", "North America", "Central America", "South America", "Caribbean"],
  "United States": ["United States"],
  "Asia": ["Asia"],
  "Europe": ["Europe"],
  "Africa": ["Africa"],
  "Middle East": ["Middle East"],
  "Oceania": ["Oceania"],
};

export function generateRoamResults(answers: RoamAnswers): RoamResult[] {
  const { vibe, region, usZone, duration, travelStyle, accommodation } = answers;

  const allowed = region ? REGION_GROUPS[region] ?? [region as Region] : null;
  const pool = allowed
    ? roamEpisodes.filter((ep) => allowed.includes(ep.region) || ep.region === "Global")
    : roamEpisodes;

  const scored = pool.map((ep) => {
    let score = 0;

    // Vibe match — highest weight
    if (vibe && ep.vibeTags.includes(vibe as VibeTag)) score += 4;

    // Duration match
    if (duration && ep.duration.includes(duration as Duration)) score += 3;

    // Travel style match
    if (travelStyle && ep.travelStyle.includes(travelStyle as TravelStyle)) score += 2;

    // Accommodation match
    if (accommodation && ep.accommodation.includes(accommodation as Accommodation)) score += 2;

    // Hard tiers, not nudges: preference points max out at 11.4 (4+3+2+2+0.4),
    // so +50/+100 guarantee zone matches outrank other US picks and direct-region
    // matches ALWAYS outrank Global fallbacks (the FAQ promises Global episodes
    // only ever surface as low-weight fallbacks).
    if (region === "United States" && usZone && ep.usZone === usZone) score += 50;
    if (allowed && allowed.includes(ep.region)) score += 100;

    // Tie-break: more diverse vibe tags = more broadly useful
    score += ep.vibeTags.length * 0.1;

    return { episode: ep, score, isTopPick: false };
  });

  // Deterministic ordering: the same answers always produce the same list,
  // so shared/saved links reproduce on every device. Ties break by episode
  // id using plain code-unit comparison (locale-independent).
  scored.sort(
    (a, b) =>
      b.score - a.score ||
      (a.episode.id < b.episode.id ? -1 : a.episode.id > b.episode.id ? 1 : 0),
  );

  if (scored.length > 0) scored[0].isTopPick = true;

  return scored;
}
